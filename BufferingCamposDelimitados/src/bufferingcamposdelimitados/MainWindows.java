    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String firstname, lastname, city, state, zipcode, adress;    
        firstname = add_firstname.getText();
        lastname = add_lastname.getText();
        city = add_city.getText();
        state = add_state.getText();
        zipcode = add_zipcode.getText();
        adress = add_adress.getText();
        if (firstname.equals("") || lastname.equals("") || city.equals("") ||
                state.equals("") || zipcode.equals("") || adress.equals("")) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorio.");
        }else {
            buffer.addPerson(new Person(lastname, firstname, adress, city, state, zipcode));
            JOptionPane.showMessageDialog(this, "Se ha agregado los datos con exito.");
            add_firstname.setText("");
            add_lastname.setText("");
            add_city.setText("");
            add_state.setText("");
            add_zipcode.setText("");
            add_adress.setText("");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void saveFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFileActionPerformed
        if (file == null) {
            JFileChooser jfc = new JFileChooser();
            int selection = jfc.showSaveDialog(this);
            if (selection == JFileChooser.APPROVE_OPTION) {
                file = jfc.getSelectedFile();
                buffer.setArchive(file);
                buffer.writePersons();
                JOptionPane.showMessageDialog(this, "Se ha guardado con exito.");
            }
        }else {
            buffer.setArchive(file);
            buffer.writePersons();
            JOptionPane.showMessageDialog(this, "Se han guardado los cambios en " + file.getName() + ".");
        }
    }//GEN-LAST:event_saveFileActionPerformed

    private void openFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileActionPerformed
        JFileChooser jfc = new JFileChooser();
        int selection = jfc.showOpenDialog(this);
        if (selection == JFileChooser.APPROVE_OPTION) {
            buffer = new Buffer();
            file = jfc.getSelectedFile();
            buffer.setArchive(file);
            if (buffer.readPersons()) {
                JOptionPane.showMessageDialog(this, "Se ha abierto el arhivo " + file.getName() + ".");
                System.out.println(buffer.getPersons());
            }else {
                JOptionPane.showMessageDialog(this, "No se pudo abrir.");
            }
        }
    }//GEN-LAST:event_openFileActionPerformed

